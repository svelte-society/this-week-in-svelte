<p>
	Our posturings, our imagined self-importance, the delusion that we have some privileged position
	in the Universe, are challenged by this point of pale light. Our planet is a lonely speck in the
	great enveloping cosmic dark. In our obscurity, in all this vastness, there is no hint that help
	will come from elsewhere to save us from ourselves.
</p>

<p>
	The Earth is the only world known so far to harbor life. There is nowhere else, at least in the
	near future, to which our species could migrate. Visit, yes. <a href="#main">Settle</a>, not yet.
	Like it or not, for the moment the Earth is where we make our stand.
</p>

<p>
	It has been said that astronomy is a humbling and character-building experience. There is perhaps
	no better demonstration of the folly of human conceits than this distant image of our tiny world.
	To me, it underscores our responsibility to deal more kindly with one another, and to preserve and
	cherish the pale blue dot, the only home we've ever known.
</p>
